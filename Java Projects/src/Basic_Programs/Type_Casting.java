package Basic_Programs;

public class Type_Casting {

	public static void main(String[] args) {
		//automatic typecasting
		int a=20;
		long l=a;
		System.out.println(l);
		byte b=120;
		int c=b;
		System.out.println(c);
		//Manual Typecasting
		int q=120;
		byte s=(byte)q;
		System.out.println(s);
		double d=23.2d;
		float f=(float)d;
		System.out.println(f);
		
	}

}
